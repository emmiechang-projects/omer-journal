// Omer starts evening of April 2, 2026 (second night of Passover)
// Day 0 = Passover (April 1 evening - April 2)
// Day 1 = April 2 sunset → April 3 sunset
// Day 2 = April 3 sunset → April 4 sunset
export const OMER_START = new Date("2026-04-02");

// Get the Omer day based on sunset (7pm local time)
export function getDayOfOmer() {
  const now = new Date();
  const hour = now.getHours();
  
  // Get today's date as just a date (no time)
  const today = new Date(now.getFullYear(), now.getMonth(), now.getDate());
  const start = new Date(2026, 3, 2); // April 2, 2026 (month is 0-indexed)
  
  // Days since April 2
  const daysSinceStart = Math.floor((today - start) / (1000 * 60 * 60 * 24));
  
  // Before 7pm: we're in the daytime portion of the current omer day
  // After 7pm: sunset happened, we advance to the next omer day
  let omerDay;
  if (hour >= 19) {
    omerDay = daysSinceStart + 1;
  } else {
    omerDay = daysSinceStart;
  }
  
  if (omerDay < 0) return 0;
  if (omerDay > 49) return 49;
  return omerDay;
}

// Get the calendar date for a given Omer day
export function getDateForOmerDay(day) {
  const date = new Date(OMER_START);
  date.setDate(date.getDate() + day);
  return date;
}

// Format today's actual date
export function formatTodayDate() {
  const now = new Date();
  return now.toLocaleDateString('en-US', { 
    weekday: 'long', 
    month: 'long', 
    day: 'numeric', 
    year: 'numeric' 
  });
}

// Format a specific omer day's date
export function formatOmerDate(day) {
  const date = getDateForOmerDay(day);
  return date.toLocaleDateString('en-US', { 
    weekday: 'long', 
    month: 'long', 
    day: 'numeric', 
    year: 'numeric' 
  });
}

// Server-side version (for cron job, uses UTC)
export function getDayOfOmerUTC() {
  const now = new Date();
  const diffMs = now - OMER_START;
  const diffDays = Math.floor(diffMs / (1000 * 60 * 60 * 24));
  if (diffDays < 0) return 0;
  if (diffDays > 49) return 49;
  return diffDays;
}

