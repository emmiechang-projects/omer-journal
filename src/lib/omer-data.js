export function getSefirahForDay(day) {
  if (day === 0) return {
    week: null, dayOf: null,
    combined: "Passover — Setting Intention",
    combinedMeaning: null,
    prompt: DAILY_PROMPTS[0],
    reflection: DAILY_REFLECTIONS[0],
    mantra: DAILY_MANTRAS[0],
  };
  const weekIdx = Math.floor((day - 1) / 7);
  const dayIdx = (day - 1) % 7;
  const week = SEFIROT_WEEKS[weekIdx];
  const dayOf = SEFIROT_DAYS[dayIdx];
  const dayMeaning = SEFIROT_MEANINGS[dayOf];
  const weekMeaning = SEFIROT_MEANINGS[week.name];
  return {
    week: week.name,
    dayOf,
    combined: `${dayOf} within ${week.name}`,
    combinedMeaning: `${dayMeaning} within ${weekMeaning}`,
    prompt: DAILY_PROMPTS[day] || `Reflect on ${dayOf} within ${week.name}.`,
    reflection: DAILY_REFLECTIONS[day] || "",
    mantra: DAILY_MANTRAS[day] || "",
  };
}

